function useI18n(lang) {
  const t = React.useCallback((k) => {
    const tbl = I18N_STRINGS[lang] || I18N_STRINGS.en;
    return tbl[k] ?? I18N_STRINGS.en[k] ?? k;
  }, [lang]);
  return { t, articles: I18N_ARTICLES[lang] || I18N_ARTICLES.en, lang };
}

